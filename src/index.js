const MORSE_TABLE = {
    '.-':     'a',
    '-...':   'b',
    '-.-.':   'c',
    '-..':    'd',
    '.':      'e',
    '..-.':   'f',
    '--.':    'g',
    '....':   'h',
    '..':     'i',
    '.---':   'j',
    '-.-':    'k',
    '.-..':   'l',
    '--':     'm',
    '-.':     'n',
    '---':    'o',
    '.--.':   'p',
    '--.-':   'q',
    '.-.':    'r',
    '...':    's',
    '-':      't',
    '..-':    'u',
    '...-':   'v',
    '.--':    'w',
    '-..-':   'x',
    '-.--':   'y',
    '--..':   'z',
    '.----':  '1',
    '..---':  '2',
    '...--':  '3',
    '....-':  '4',
    '.....':  '5',
    '-....':  '6',
    '--...':  '7',
    '---..':  '8',
    '----.':  '9',
    '-----':  '0',
};

function decode(expr) {
    let coded = expr; // initialization...
    let decoded = "";

    while (coded) {
        let piece = coded.slice(0, 10);
        if (piece === "**********") decoded += " ";
        else {
            piece = piece.replace(/10/g, ".").replace(/11/g, "-").replace(/0/g, "");
            decoded += MORSE_TABLE[piece];
        }
        coded = coded.slice(10);
    }
    return decoded;
}

module.exports = {
    decode
}